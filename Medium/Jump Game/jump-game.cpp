class Solution {
  public:
    int canReach(int A[], int N) {
        int final_position=N-1;
        for(int position=final_position-1;position>=0;position--){
            if(position+A[position]>=final_position){
                final_position=position;
            }
        }
        return final_position==0;
    }
};

